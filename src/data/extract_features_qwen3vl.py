"""
extract_features_qwen3vl.py
=============================
Extracts Qwen3-VL-Embedding-2B image features from PNG images
previously generated by save_images.py.

Qwen3-VL-Embedding-2B specs:
    - Based on Qwen3-VL-2B vision-language model
    - Hidden dim: 1536
    - Total decoder layers: 28
    - Uses last-token pooling for embeddings

Output (saved into NSD/data/nsd/subj0{sub}/):
  1. Pooled (last-token embedding, last layer):
     - nsd_qwen3vl_pool_{mode}_sub{sub}.npy        (N, 1536)     float16
  2. Multilayer (last-token from selected layers):
     - nsd_qwen3vl_multi_{mode}_sub{sub}.npy        (N, L, 1536)  float16
     where L = number of selected layers

Usage:
  python src/data/extract_features_qwen3vl.py --subjects 1
  python src/data/extract_features_qwen3vl.py --subjects 1 --layers 6 13 20 27
  python src/data/extract_features_qwen3vl.py --subjects 1 --batch_size 8
"""

import argparse
import os
import sys
import numpy as np
import torch
import torch.nn.functional as F
from PIL import Image
from tqdm import tqdm

# ==============================================================================
# Configuration
# ==============================================================================
SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
BASE_DIR = os.path.abspath(os.path.join(SCRIPT_DIR, '../../NSD/data'))
PROCESSED_DATA_DIR = os.path.join(BASE_DIR, 'nsd')

# Model
MODEL_NAME = "Qwen/Qwen3-VL-Embedding-2B"
HIDDEN_DIM = 2048  # Qwen3-VL-2B hidden size

# Defaults
DEFAULT_SUBJECTS = [1, 2, 5, 7]
DEFAULT_BATCH_SIZE = 8  # Small batch due to VLM memory requirements
DEFAULT_LAYERS = [6, 13, 20, 27]  # Qwen3-VL-2B has 28 layers (0-27)


def parse_args():
    parser = argparse.ArgumentParser(description="Extract Qwen3-VL-Embedding-2B features")
    parser.add_argument('--subjects', type=int, nargs='+', default=DEFAULT_SUBJECTS,
                        help='Subject numbers to process (default: 1 2 5 7)')
    parser.add_argument('--batch_size', type=int, default=DEFAULT_BATCH_SIZE,
                        help=f'Batch size (default: {DEFAULT_BATCH_SIZE})')
    parser.add_argument('--device', type=str, default='',
                        help='Device (default: auto-detect)')
    parser.add_argument('--layers', type=int, nargs='+', default=DEFAULT_LAYERS,
                        help=f'Layer indices for multilayer extraction (default: {DEFAULT_LAYERS})')
    parser.add_argument('--model_path', type=str, default=MODEL_NAME,
                        help=f'Model name or local path (default: {MODEL_NAME})')
    return parser.parse_args()


def main():
    args = parse_args()

    # ── Device ──
    device = args.device if args.device else ('cuda' if torch.cuda.is_available() else 'cpu')
    print(f"Device: {device}")

    # ── tf32 for faster computation ──
    torch.backends.cuda.matmul.allow_tf32 = True

    # ── Load model and processor ──
    print(f"Loading {args.model_path}...")

    from transformers import Qwen3VLProcessor
    from qwen_vl_utils import process_vision_info

    # Import Qwen3VLForEmbedding from the model's scripts
    # We need to download/access the custom class
    try:
        from transformers import AutoModel, AutoConfig
        # Try loading with trust_remote_code
        model = AutoModel.from_pretrained(
            args.model_path,
            trust_remote_code=True,
            torch_dtype=torch.float16,
        ).to(device).eval()
    except Exception:
        # Fallback: use Qwen3VLModel directly and extract hidden states
        from transformers import Qwen3VLForConditionalGeneration
        model = Qwen3VLForConditionalGeneration.from_pretrained(
            args.model_path,
            torch_dtype=torch.float16,
            output_hidden_states=True,
        ).to(device).eval()

    processor = Qwen3VLProcessor.from_pretrained(
        args.model_path,
        padding_side='right',
    )

    # Qwen3VLConfig uses nested text_config for LLM params
    text_cfg = model.config.text_config if hasattr(model.config, 'text_config') else model.config
    num_layers = text_cfg.num_hidden_layers
    hidden_dim = text_cfg.hidden_size
    print(f"Model loaded. Hidden dim: {hidden_dim}, Layers: {num_layers}")
    print(f"Multilayer extraction layers: {args.layers}")

    # ── No need for hooks: Qwen3-VL natively supports output_hidden_states=True ──

    # ── Helper: pooling last token ──
    def pooling_last(hidden_state, attention_mask):
        """Extract the last non-padding token's hidden state."""
        flipped = attention_mask.flip(dims=[1])
        last_positions = flipped.argmax(dim=1)
        col = attention_mask.shape[1] - last_positions - 1
        row = torch.arange(hidden_state.shape[0], device=hidden_state.device)
        return hidden_state[row, col]

    # ── Helper: process a batch of images ──
    def extract_batch(image_paths):
        """Process a batch of images and return pooled + multilayer embeddings."""
        # Build conversations for each image
        conversations_batch = []
        for img_path in image_paths:
            conversation = [
                {"role": "system", "content": [{"type": "text", "text": "Represent the user's input."}]},
                {"role": "user", "content": [
                    {"type": "image", "image": f"file://{img_path}",
                     "min_pixels": 4 * 28 * 28, "max_pixels": 1800 * 28 * 28},
                ]}
            ]
            conversations_batch.append(conversation)

        # Apply chat template
        texts = processor.apply_chat_template(
            conversations_batch, add_generation_prompt=True, tokenize=False
        )

        # Process vision info
        all_images = []
        for conv in conversations_batch:
            try:
                imgs, _, _ = process_vision_info(
                    conv, return_video_metadata=True, return_video_kwargs=True
                )
                if imgs:
                    all_images.extend(imgs)
            except Exception:
                imgs, _ = process_vision_info(conv)
                if imgs:
                    all_images.extend(imgs)

        # Tokenize
        inputs = processor(
            text=texts,
            images=all_images if all_images else None,
            truncation=True,
            max_length=8192,
            padding=True,
            return_tensors='pt',
        )
        inputs = {k: v.to(device) for k, v in inputs.items()}

        with torch.no_grad(), torch.amp.autocast('cuda', enabled=(device == 'cuda')):
            outputs = model(**inputs, output_hidden_states=True)

            # Get last hidden state
            if hasattr(outputs, 'last_hidden_state'):
                last_hidden = outputs.last_hidden_state
            elif hasattr(outputs, 'hidden_states') and outputs.hidden_states is not None:
                last_hidden = outputs.hidden_states[-1]

            # Pool: extract last-token embedding
            attn_mask = inputs['attention_mask']
            pooled = pooling_last(last_hidden, attn_mask)  # (B, hidden_dim)
            pooled = F.normalize(pooled.float(), p=2, dim=-1)

            # Multilayer: extract last-token from intermediate layers
            multi_embeddings = None
            if hasattr(outputs, 'hidden_states') and outputs.hidden_states is not None:
                multi_list = []
                # outputs.hidden_states[0] is embedding layer
                # outputs.hidden_states[1:] are the decoder layers
                # So layer_idx 0 is outputs.hidden_states[1]
                for layer_idx in args.layers:
                    if layer_idx + 1 < len(outputs.hidden_states):
                        layer_hidden = outputs.hidden_states[layer_idx + 1]
                        layer_pooled = pooling_last(layer_hidden, attn_mask)  # (B, hidden_dim)
                        layer_pooled = F.normalize(layer_pooled.float(), p=2, dim=-1)
                        multi_list.append(layer_pooled)
                if multi_list:
                    multi_embeddings = torch.stack(multi_list, dim=1)  # (B, L, hidden_dim)

        return pooled, multi_embeddings

    # ── Extract features per subject ──
    for subj in args.subjects:
        save_path = os.path.join(PROCESSED_DATA_DIR, f'subj0{subj}')
        if not os.path.exists(save_path):
            print(f"\nSubject {subj} folder not found at {save_path}, skipping...")
            continue

        print(f"\n{'='*60}")
        print(f"Processing Subject {subj}...")
        print(f"{'='*60}")

        for mode in ['train', 'test']:
            image_dir = os.path.join(save_path, f'{mode}_img')
            if not os.path.exists(image_dir):
                print(f"  {mode}_img not found, skipping...")
                continue

            # Build sorted file list: 0.png, 1.png, 2.png, ...
            num_images = len([f for f in os.listdir(image_dir) if f.endswith('.png')])
            image_paths = [
                os.path.join(image_dir, f'{i}.png') for i in range(num_images)
            ]
            print(f'  {mode.capitalize()} images: {num_images}')

            all_pooled = []
            all_multi = []

            # Process in batches
            for start_idx in tqdm(range(0, num_images, args.batch_size),
                                  desc=f"  Extracting {mode}"):
                end_idx = min(start_idx + args.batch_size, num_images)
                batch_paths = image_paths[start_idx:end_idx]

                pooled, multi = extract_batch(batch_paths)

                all_pooled.append(pooled.cpu().half())
                if multi is not None:
                    all_multi.append(multi.cpu().half())

                torch.cuda.empty_cache()

            if all_pooled:
                # 1. Pooled embedding (last layer, last token)
                pool_np = torch.cat(all_pooled, dim=0).numpy()
                out_pool = os.path.join(save_path, f'nsd_qwen3vl_pool_{mode}_sub{subj}.npy')
                np.save(out_pool, pool_np)
                print(f"  ✅ Pooled:  {out_pool}  shape={pool_np.shape}  {pool_np.nbytes/1e6:.1f}MB")
                del pool_np, all_pooled

                # 2. Multilayer (last token from selected layers)
                if all_multi:
                    multi_np = torch.cat(all_multi, dim=0).numpy()
                    out_multi = os.path.join(save_path, f'nsd_qwen3vl_multi_{mode}_sub{subj}.npy')
                    np.save(out_multi, multi_np)
                    print(f"  ✅ Multi:   {out_multi}  shape={multi_np.shape}  {multi_np.nbytes/1e6:.1f}MB")
                    del multi_np, all_multi

    print(f"\n{'='*60}")
    print("Done. Qwen3-VL feature extraction completed successfully!")
    print(f"{'='*60}")


if __name__ == '__main__':
    main()
