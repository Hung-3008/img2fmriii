def load_model_and_processor(model_key, model_path_override=None, device='cuda'):
    config = MODEL_CONFIGS[model_key]
    model_type = config['model_type']
    model_name_or_path = model_path_override if model_path_override else config['model_name']
    
    model = None
    processor_or_transform = None
    
    if model_type == 'dinov2':
        print(f"Loading {model_name_or_path} from torch.hub...")
        model = torch.hub.load('facebookresearch/dinov2', model_name_or_path)
        model = model.to(device).eval()
        
        # DINOv2 transform
        processor_or_transform = transforms.Compose([
            transforms.Resize(224, interpolation=transforms.InterpolationMode.BICUBIC),
            transforms.CenterCrop(224),
            transforms.ToTensor(),
            transforms.Normalize(mean=IMAGENET_MEAN, std=IMAGENET_STD),
        ])
        
    elif model_type == 'resnet':
        print(f"Loading {model_name_or_path} from torchvision...")
        if model_name_or_path == 'resnet50':
            weights = models.ResNet50_Weights.IMAGENET1K_V2
            model = models.resnet50(weights=weights)
        elif model_name_or_path == 'resnet101':
            weights = models.ResNet101_Weights.IMAGENET1K_V2
            model = models.resnet101(weights=weights)
        else:
            raise ValueError(f"Unknown resnet model: {model_name_or_path}")
            
        model = model.to(device).eval()
        
        # ResNet transform
        processor_or_transform = transforms.Compose([
            transforms.Resize(256, interpolation=transforms.InterpolationMode.BILINEAR),
            transforms.CenterCrop(224),
            transforms.ToTensor(),
            transforms.Normalize(mean=IMAGENET_MEAN, std=IMAGENET_STD),
        ])
        
    elif model_type == 'sam':
        from transformers import SamModel, SamProcessor
        print(f"Loading {model_name_or_path} from transformers...")
        processor_or_transform = SamProcessor.from_pretrained(model_name_or_path)
        model = SamModel.from_pretrained(model_name_or_path)
        model = model.to(device).eval()
        
    elif model_type == 'sdxl_clip':
        # Append SGM path
        script_dir = os.path.dirname(os.path.abspath(__file__))
        sgm_path = os.path.abspath(os.path.join(script_dir, '../../NSD/notes/SynBrain/src/sdxl'))
        if sgm_path not in sys.path:
            sys.path.append(sgm_path)
            
        from generative_models.sgm.modules.encoders.modules import FrozenOpenCLIPImageEmbedder
        
        # Determine weights path
        if model_path_override:
            open_clip_model_path = model_path_override
        else:
            base_data_dir = os.path.abspath(os.path.join(script_dir, '../../NSD/data'))
            open_clip_model_path = os.path.abspath(os.path.join(base_data_dir, '../checkpoints/sdxl_unclip/open_clip_pytorch_model.bin'))
            
        if not os.path.exists(open_clip_model_path):
            print(f"ERROR: open_clip_pytorch_model.bin not found at: {open_clip_model_path}")
            print("Please download from: https://huggingface.co/laion/CLIP-ViT-bigG-14-laion2B-39B-b160k/resolve/main/open_clip_pytorch_model.bin")
            sys.exit(1)
            
        print(f"Loading FrozenOpenCLIPImageEmbedder from: {open_clip_model_path}...")
        model = FrozenOpenCLIPImageEmbedder(
            arch="ViT-bigG-14",
            version=open_clip_model_path,
            output_tokens=True,
            only_tokens=False,
        )
        model = model.to(device).eval()
        processor_or_transform = None
        
    elif model_type == 'qwen3vl':
        from transformers import Qwen3VLProcessor
        print(f"Loading {model_name_or_path}...")
        
        try:
            from transformers import AutoModel
            model = AutoModel.from_pretrained(
                model_name_or_path,
                trust_remote_code=True,
                torch_dtype=torch.float16,
            ).to(device).eval()
        except Exception:
            from transformers import Qwen3VLForConditionalGeneration
            model = Qwen3VLForConditionalGeneration.from_pretrained(
                model_name_or_path,
                torch_dtype=torch.float16,
                output_hidden_states=True,
            ).to(device).eval()
            
        processor_or_transform = Qwen3VLProcessor.from_pretrained(
            model_name_or_path,
            padding_side='right',
        )
        
    else:
        raise ValueError(f"Unsupported model_type: {model_type}")
        
    return model, processor_or_transform

def extract_qwen_batch(model, processor, image_paths, layers, device):
    conversations_batch = []
    for img_path in image_paths:
        conversation = [
            {"role": "system", "content": [{"type": "text", "text": "Represent the user's input."}]},
            {"role": "user", "content": [
                {"type": "image", "image": f"file://{img_path}",
                 "min_pixels": 4 * 28 * 28, "max_pixels": 1800 * 28 * 28},
            ]}
        ]
        conversations_batch.append(conversation)

    from qwen_vl_utils import process_vision_info
    
    texts = processor.apply_chat_template(
        conversations_batch, add_generation_prompt=True, tokenize=False
    )
    
    all_images = []
    for conv in conversations_batch:
        try:
            imgs, _, _ = process_vision_info(
                conv, return_video_metadata=True, return_video_kwargs=True
            )
            if imgs:
                all_images.extend(imgs)
        except Exception:
            imgs, _ = process_vision_info(conv)
            if imgs:
                all_images.extend(imgs)
                
    inputs = processor(
        text=texts,
        images=all_images if all_images else None,
        truncation=True,
        max_length=8192,
        padding=True,
        return_tensors='pt',
    )
    inputs = {k: v.to(device) for k, v in inputs.items()}
    
    with torch.no_grad(), torch.amp.autocast('cuda', enabled=(device == 'cuda')):
        outputs = model(**inputs, output_hidden_states=True)
        
        if hasattr(outputs, 'last_hidden_state'):
            last_hidden = outputs.last_hidden_state
        elif hasattr(outputs, 'hidden_states') and outputs.hidden_states is not None:
            last_hidden = outputs.hidden_states[-1]
            
        attn_mask = inputs['attention_mask']
        
        # Helper function
        def pooling_last(hidden_state, attention_mask):
            flipped = attention_mask.flip(dims=[1])
            last_positions = flipped.argmax(dim=1)
            col = attention_mask.shape[1] - last_positions - 1
            row = torch.arange(hidden_state.shape[0], device=hidden_state.device)
            return hidden_state[row, col]
            
        pooled = pooling_last(last_hidden, attn_mask)
        pooled = F.normalize(pooled.float(), p=2, dim=-1)
        
        multi_embeddings = None
        if hasattr(outputs, 'hidden_states') and outputs.hidden_states is not None:
            multi_list = []
            for layer_idx in layers:
                if layer_idx + 1 < len(outputs.hidden_states):
                    layer_hidden = outputs.hidden_states[layer_idx + 1]
                    layer_pooled = pooling_last(layer_hidden, attn_mask)
                    layer_pooled = F.normalize(layer_pooled.float(), p=2, dim=-1)
                    multi_list.append(layer_pooled)
            if multi_list:
                multi_embeddings = torch.stack(multi_list, dim=1)
                
    return pooled, multi_embeddings

