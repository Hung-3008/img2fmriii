def extract_qwen_batch(model, processor, image_paths, layers, device):
    conversations_batch = []
    for img_path in image_paths:
        conversation = [
            {"role": "system", "content": [{"type": "text", "text": "Represent the user's input."}]},
            {"role": "user", "content": [
                {"type": "image", "image": f"file://{img_path}",
                 "min_pixels": 4 * 28 * 28, "max_pixels": 1800 * 28 * 28},
            ]}
        ]
        conversations_batch.append(conversation)

    from qwen_vl_utils import process_vision_info
    
    texts = processor.apply_chat_template(
        conversations_batch, add_generation_prompt=True, tokenize=False
    )
    
    all_images = []
    for conv in conversations_batch:
        try:
            imgs, _, _ = process_vision_info(
                conv, return_video_metadata=True, return_video_kwargs=True
            )
            if imgs:
                all_images.extend(imgs)
        except Exception:
            imgs, _ = process_vision_info(conv)
            if imgs:
                all_images.extend(imgs)
                
    inputs = processor(
        text=texts,
        images=all_images if all_images else None,
        truncation=True,
        max_length=8192,
        padding=True,
        return_tensors='pt',
    )
    inputs = {k: v.to(device) for k, v in inputs.items()}
    
    with torch.no_grad(), torch.amp.autocast('cuda', enabled=(device == 'cuda')):
        outputs = model(**inputs, output_hidden_states=True)
        
        if hasattr(outputs, 'last_hidden_state'):
            last_hidden = outputs.last_hidden_state
        elif hasattr(outputs, 'hidden_states') and outputs.hidden_states is not None:
            last_hidden = outputs.hidden_states[-1]
            
        attn_mask = inputs['attention_mask']
        
        # Helper function
        def pooling_last(hidden_state, attention_mask):
            flipped = attention_mask.flip(dims=[1])
            last_positions = flipped.argmax(dim=1)
            col = attention_mask.shape[1] - last_positions - 1
            row = torch.arange(hidden_state.shape[0], device=hidden_state.device)
            return hidden_state[row, col]
            
        pooled = pooling_last(last_hidden, attn_mask)
        pooled = F.normalize(pooled.float(), p=2, dim=-1)
        
        multi_embeddings = None
        if hasattr(outputs, 'hidden_states') and outputs.hidden_states is not None:
            multi_list = []
            for layer_idx in layers:
                if layer_idx + 1 < len(outputs.hidden_states):
                    layer_hidden = outputs.hidden_states[layer_idx + 1]
                    layer_pooled = pooling_last(layer_hidden, attn_mask)
                    layer_pooled = F.normalize(layer_pooled.float(), p=2, dim=-1)
                    multi_list.append(layer_pooled)
            if multi_list:
                multi_embeddings = torch.stack(multi_list, dim=1)
                
    return pooled, multi_embeddings


def extract_clip_multilayer(model, img, layers, multi_mode):
    """Per-layer features from the OpenCLIP visual transformer.

    Uses the official ``forward_intermediates`` API (open_clip >= 2.26), which is
    the direct analogue of DINOv2's ``get_intermediate_layers``. ``ln_post`` is
    applied to every intermediate (``normalize_intermediates=True``) so the output
    matches DINOv2 (``norm=True``) and CLIP's own final-layer tokens.

    Args:
        model: FrozenOpenCLIPImageEmbedder (preprocessing happens here).
        img:   raw image batch (B, 3, H, W); ``model.preprocess`` is applied.
        layers: list of block indices to take.
        multi_mode: 'spatial' → (B, L, 1+N, D) CLS+patch per layer;
                    'cls'     → (B, L, D)       CLS-only per layer.
    """
    visual = model.model.visual
    img_pp = model.preprocess(img)
    out = visual.forward_intermediates(
        img_pp,
        indices=layers,
        normalize_intermediates=True,
        output_fmt='NLC',
        output_extra_tokens=True,
        intermediates_only=True,
    )
    patches = out['image_intermediates']          # list[L] of (B, N, D)
    prefix = out['image_intermediates_prefix']    # list[L] of (B, 1, D)
    if multi_mode == 'cls':
        return torch.stack([c[:, 0] for c in prefix], dim=1)        # (B, L, D)
    return torch.stack(
        [torch.cat([c, s], dim=1) for c, s in zip(prefix, patches)],
        dim=1,
    )                                                               # (B, L, 1+N, D)


def pool_spatial_grid(multi, factor, n_cls=1):
    """Average-pool the ViT patch grid of a spatial multilayer tensor.

    ``multi`` is (B, L, n_cls + G*G, D); the first ``n_cls`` prefix tokens (CLS)
    pass through unchanged and the G*G patch tokens are reshaped to the G*G grid
    and averaged in ``factor``*``factor`` blocks → (B, L, n_cls + (G/factor)**2, D).
    The mean is computed in float32 for stability (caller casts back to fp16).
    """
    B, L, T, D = multi.shape
    n_patch = T - n_cls
    g = int(round(n_patch ** 0.5))
    assert g * g == n_patch, f"patch count {n_patch} (T={T} - cls={n_cls}) is not a perfect square"
    assert g % factor == 0, f"grid {g}x{g} not divisible by pool factor {factor}"
    go = g // factor
    cls = multi[:, :, :n_cls, :].float()
    patch = multi[:, :, n_cls:, :].float().reshape(B, L, g, g, D)
    patch = patch.reshape(B, L, go, factor, go, factor, D).mean(dim=(3, 5))
    patch = patch.reshape(B, L, go * go, D)
    return torch.cat([cls, patch], dim=2)


def copy_test_features(ref_subj, dst_subj, ref_out_dir, dst_out_dir,
                       file_prefix, multi_suffix, has_tokens):
    """Reuse one subject's test features for another.

    NSD's test set is the shared-1000 stimuli — byte-identical across subjects — so
    test image features only need extracting once. For the other subjects we copy
    the reference subject's test files, renaming the ``_sub{ref}`` suffix. (fMRI and
    roi_meta stay per-subject and are untouched.)
    """
    candidates = [f'nsd_{file_prefix}_pool_test_sub{ref_subj}.npy']
    if has_tokens:
        candidates.append(f'nsd_{file_prefix}_test_sub{ref_subj}.npy')
    candidates.append(f'nsd_{file_prefix}{multi_suffix}test_sub{ref_subj}.npy')

    copied = 0
    for fn in candidates:
        src = os.path.join(ref_out_dir, fn)
        if not os.path.exists(src):
            print(f"  ⚠️  ref test file missing, skipped: {src}")
            continue
        dst_fn = fn.replace(f'_sub{ref_subj}.npy', f'_sub{dst_subj}.npy')
        dst = os.path.join(dst_out_dir, dst_fn)
        shutil.copyfile(src, dst)
        copied += 1
        print(f"  📋 Copied test: {fn} → {dst_fn} ({os.path.getsize(dst)/1e6:.1f}MB)")
    return copied

