class UnifiedImageDataset(Dataset):
    def __init__(self, image_paths, model_type, transform_or_processor=None):
        self.image_paths = image_paths
        self.model_type = model_type
        self.transform_or_processor = transform_or_processor

    def __len__(self):
        return len(self.image_paths)

    def __getitem__(self, idx):
        img_path = self.image_paths[idx]
        img = Image.open(img_path)
        
        if self.model_type in ['dinov2', 'dinov3', 'resnet']:
            img = img.convert('RGB')
            if self.transform_or_processor:
                img = self.transform_or_processor(img)
            return img
        elif self.model_type == 'sam':
            img = img.convert('RGB')
            inputs = self.transform_or_processor(images=img, return_tensors="pt")
            return inputs['pixel_values'].squeeze(0)
        elif self.model_type == 'sdxl_clip':
            img = img.convert('RGB')
            img = TF.to_tensor(img).float()
            return img
        else:
            raise ValueError(f"Unsupported model_type in dataset: {self.model_type}")

